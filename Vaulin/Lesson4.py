#Задача 6 (Тернарный оператор)
# У вас есть переменная temperature = 25.
# Напишите одну строку кода, которая использует тернарный оператор для присвоения переменной status строкового значения:
# — "Тепло", если temperature больше 20.
# — "Прохладно" в противном случае.
# Затем выведите значение status на экран.

