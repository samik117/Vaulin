#Задача 4 (Оператор elif)
# Напишите программу, которая просит пользователя ввести целое число и сохраняет его в переменную number.
# Используя конструкцию if-elif-else, определите и выведите на экран категорию числа:
# — Если number меньше 0, выведите "Отрицательное".
# — Иначе если number равно 0, выведите "Ноль".
# — Иначе (во всех остальных случаях) выведите "Положительное".

number = int(input("Введите целое число: "))

if number < 0:
    print("Отрицательное")

elif number == 0:
    print("Ноль")

else:
    print("Положительное")

