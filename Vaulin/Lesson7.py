#Задача 5 (Работа с файлами)
# Напишите программу, которая открывает файл log.txt в режиме добавления ("a").
# Затем записывает в него текущую дату и время в следующем формате: [2024-01-01 10:30:00] Запуск программы.
# Для получения даты и времени используйте модуль datetime (предварительно импортируйте его).
# Не забудьте закрыть файл.
# Подсказка: для получения текущей даты и времени используйте datetime.datetime.now().
# Для форматирования в нужную строку — метод .strftime().

import datetime
file = open("log.txt", "a", encoding = 'utf-8')

nowdata = datetime.datetime.now()

new_format_time = nowdata.strftime("[%Y-%m-%d %H:%M:%S]")

file.write("\n" + new_format_time + " Запуск программы")

file.close()

