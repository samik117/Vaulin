#Задача 3 (Работа с файлами)
# Напишите программу, которая открывает файл numbers.txt в режиме записи ("w").
# Затем с помощью цикла for и функции range() запишите в файл числа от 1 до 5, каждое с новой строки.
# Не забудьте закрыть файл.
# Подсказка: метод .write() не добавляет автоматически перевод строки. Вам нужно явно добавлять "\n".

file = open("numbers.txt", "w", encoding='utf-8')

for numbers in range(1, 6):

    file.write(str(numbers) + "\n")

file.close()

