#Задача 1 (Цикл for)
# Дан список: animals = ["собака", "кошка", "попугай", "хомяк"].
# Напишите цикл for, который проходит по этому списку и выводит каждый элемент на новой строке.

animals = ["собака", "кошка", "попугай", "хомяк"]

for animal in animals:
    print(animal)

