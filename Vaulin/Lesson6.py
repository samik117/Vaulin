#Задача 5 (Остановка циклов break)
# Дан список чисел: numbers = [3, 7, 10, 15, 22, 5, 8].
# Напишите цикл for, который проходит по этому списку.
# Внутри цикла проверяйте каждый элемент:
# — Если текущее число больше 20, немедленно завершите выполнение цикла с помощью оператора break.
# — Иначе выводите это число на экран.
# Цикл должен проверить числа по порядку и остановиться, как только встретит число > 20.



numbers = [3, 7, 10, 15, 22, 5, 8]

for number in numbers:
    if number > 20:
        break
    print(number)

#Задача 6 (Цикл for)
# Дан список строк: words = ["яблоко", "груша", "слива", "вишня"].
# Напишите цикл for, который проходит по этому списку и для каждого элемента выводит строку в формате:
# "Фрукт: [слово]", где [слово] — текущий элемент списка.
# Например, для первого элемента вывод должен быть: "Фрукт: яблоко".

words = ["яблоко", "груша", "слива", "вишня"]

