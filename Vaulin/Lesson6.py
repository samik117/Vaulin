#Задача 3 (Цикл while)
# Напишите цикл while, который начинается с x = 1 и увеличивает x на 1 на каждом шаге.
# Цикл должен работать, пока x меньше или равно 5. Внутри цикла выводите текущее значение x.
# После завершения цикла выведите строку "Цикл окончен".

x = 1

while x <= 5:

    print(x)
    x += 1

print("Цикл окончен")

